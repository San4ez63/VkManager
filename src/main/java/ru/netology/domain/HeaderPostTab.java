package ru.netology.domain;

public class HeaderPostTab {
    private String currentsPost;
    private String interestsPost;
    private String searchPost;

    public String getCurrentsPost() {
        return currentsPost;
    }

    public void setCurrentsPost(String currentsPost) {
        this.currentsPost = currentsPost;
    }

    public String getInterestsPost() {
        return interestsPost;
    }

    public void setInterestsPost(String interestsPost) {
        this.interestsPost = interestsPost;
    }

    public String getSearchPost() {
        return searchPost;
    }

    public void setSearchPost(String searchPost) {
        this.searchPost = searchPost;
    }
}
