package ru.netology.domain;

public class MenuWrap {
    private String complainTab;
    private String addTab;
    private String deleteTab;

    public String getComplainTab() {
        return complainTab;
    }

    public void setComplainTab(String complainTab) {
        this.complainTab = complainTab;
    }

    public String getAddTab() {
        return addTab;
    }

    public void setAddTab(String addTab) {
        this.addTab = addTab;
    }

    public String getDeleteTab() {
        return deleteTab;
    }

    public void setDeleteTab(String deleteTab) {
        this.deleteTab = deleteTab;
    }
}
